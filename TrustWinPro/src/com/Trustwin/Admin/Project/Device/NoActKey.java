package com.Trustwin.Admin.Project.Device;

public class NoActKey {
	
	private int idx;
	
	private int FirstKey;
	
	private int SecondKey;
	
	private int ThirdKey;
	
	private int FourthKey;
	
	private int DeviceID;

	public int getIdx() {
		return idx;
	}

	public void setIdx(int idx) {
		this.idx = idx;
	}

	public int getFirstKey() {
		return FirstKey;
	}

	public void setFirstKey(int firstKey) {
		FirstKey = firstKey;
	}

	public int getSecondKey() {
		return SecondKey;
	}

	public void setSecondKey(int secondKey) {
		SecondKey = secondKey;
	}

	public int getThirdKey() {
		return ThirdKey;
	}

	public void setThirdKey(int thirdKey) {
		ThirdKey = thirdKey;
	}

	public int getFourthKey() {
		return FourthKey;
	}

	public void setFourthKey(int fourthKey) {
		FourthKey = fourthKey;
	}

	public int getDeviceID() {
		return DeviceID;
	}

	public void setDeviceID(int deviceID) {
		DeviceID = deviceID;
	}
	
}
