package com.Trustwin.Admin.Project.Category;


public class Category {
	
	private int idx;
	
	private String name;
	
	private int upnumber;
	
	private int depth;

	
	
	// Get and Set
	
	public int getIdx() {
		return idx;
	}

	public void setIdx(int idx) {
		this.idx = idx;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getUpnumber() {
		return upnumber;
	}

	public void setUpnumber(int upnumber) {
		this.upnumber = upnumber;
	}

	public int getDepth() {
		return depth;
	}

	public void setDepth(int depth) {
		this.depth = depth;
	}
	
	
	
}
